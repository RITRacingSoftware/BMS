        dst_p[2] |= pack_left_shift_u16(src_p->bms_thermistor_voltages_tm2, 4u, 0xf0u);
        dst_p[3] |= pack_right_shift_u16(src_p->bms_thermistor_voltages_tm2, 4u, 0x1fu);
        dst_p[3] |= pack_left_shift_u16(src_p->bms_thermistor_voltages_tm3, 5u, 0xe0u);
        dst_p[4] |= pack_right_shift_u16(src_p->bms_thermistor_voltages_tm3, 3u, 0x3fu);
        dst_p[4] |= pack_left_shift_u16(src_p->bms_thermistor_voltages_tm4, 6u, 0xc0u);
        dst_p[5] |= pack_right_shift_u16(src_p->bms_thermistor_voltages_tm4, 2u, 0x7fu);
        dst_p[5] |= pack_left_shift_u16(src_p->bms_thermistor_voltages_tm5, 7u, 0x80u);
